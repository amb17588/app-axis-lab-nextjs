#!/usr/bin/env node
/**
 * Patches .open-next/cloudflare/init.js after every `npm run build:cf`.
 *
 * Root cause: OpenNext's edge converter stores the POST request body as a
 * Node.js Buffer and passes it to `new Request(url, { body: Buffer })`.
 * The Cloudflare Workers runtime does not recognise a Node.js Buffer as a
 * valid BodyInit — it coerces it via Object.prototype.toString(), producing
 * "[object Object]" as the body bytes.  Route handlers then receive that
 * literal string instead of the original payload, so JSON.parse fails.
 *
 * Fix: detect Buffers with Buffer.isBuffer() and copy the raw bytes into a
 * native Uint8Array, which CF Workers always accepts as a valid BodyInit.
 */

const fs = require('fs')
const path = require('path')

const initPath = path.join(__dirname, '..', '.open-next', 'cloudflare', 'init.js')

if (!fs.existsSync(initPath)) {
  console.error('[patch-opennext] init.js not found — skipping patch')
  process.exit(0)
}

const original = fs.readFileSync(initPath, 'utf8')

// The exact block generated by @opennextjs/cloudflare that we need to replace.
const OLD = `        Object.defineProperty(init2, "body", {
          // @ts-ignore
          value: init2.body instanceof stream.Readable ? ReadableStream.from(init2.body) : init2.body
        });`

const NEW = `        let _body = init2.body;
        // CF Workers does not recognise a Node.js Buffer as a valid BodyInit —
        // it coerces it via Object.prototype.toString(), producing "[object Object]".
        // Detect Buffers with Buffer.isBuffer() and copy the bytes into a native
        // Uint8Array, which CF Workers always accepts as a BodyInit.
        if (_body instanceof stream.Readable) {
          _body = ReadableStream.from(_body);
        } else if (
          typeof Buffer !== "undefined" &&
          Buffer.isBuffer(_body)
        ) {
          const len = _body.length;
          const bytes = new Uint8Array(len);
          for (let i = 0; i < len; i++) bytes[i] = _body[i];
          _body = bytes;
        }
        Object.defineProperty(init2, "body", {
          // @ts-ignore
          value: _body,
          writable: true,
          enumerable: true,
          configurable: true,
        });`

if (!original.includes(OLD)) {
  if (original.includes('Buffer.isBuffer')) {
    console.log('[patch-opennext] init.js already patched — skipping.')
  } else {
    console.warn('[patch-opennext] Patch target not found in init.js — the OpenNext version may have changed. Manual review needed.')
  }
  process.exit(0)
}

const patched = original.replace(OLD, NEW)
fs.writeFileSync(initPath, patched, 'utf8')
console.log('[patch-opennext] init.js patched successfully.')
